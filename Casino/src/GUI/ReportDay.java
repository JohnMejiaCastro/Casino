    public void earningMoney() {

        SimpleDateFormat fo = new SimpleDateFormat("dd/MM/YYYY hh:mm:ss");

        XMLMachineFile machineFile = new XMLMachineFile();
        List<Machine> machineList = machineFile.readFile("machine.xml");

        XMLReportDay dataFile = new XMLReportDay();
        List<DataDayReport> reportList = dataFile.readFile(this.filename);
        Date diaAnterior = datDayReport.getDate();
        diaAnterior.setTime(diaAnterior.getTime() - 24 * 1000 * 60 * 60);

        //JOptionPane.showMessageDialog(null, fo.format(datDayReport.getDate()) + "\n" + fo.format(diaAnterior));
        //for (DataDayReport List : mach.getDataDayReportList()) {
        for (Machine machine : machineList) {
            long todayInCounter = 0;
            long yesterdayInCounter = 0;
            long todayOutCounter = 0;
            long yesterdayOutCounter = 0;

            for (DataDayReport report : reportList) {
                if (machine.getNumberMachine() == report.getNumberMachine()) {
                    if (report.getDayDate().equals(datDayReport.getDate())) {
                        todayInCounter = report.getMachineInputDayCurrent();
                        todayOutCounter = report.getMachineOutDayCurrent();
                    }
                    if (report.getDayDate().equals(diaAnterior)) {
                        yesterdayInCounter = report.getMachineInputDayCurrent();
                        yesterdayOutCounter = report.getMachineOutDayCurrent();
                    }
                }
            }

            long inCounter = todayInCounter - yesterdayInCounter;
            long outCounter = todayOutCounter - yesterdayOutCounter;
            long result = (inCounter - outCounter) * machine.getBetValue();

            modelo.insertRow(con, new Object[]{});
            modelo.setValueAt(machine.getNumberMachine(), con, 0);
            modelo.setValueAt(result, con, 1);
            con++;

        }

    }

